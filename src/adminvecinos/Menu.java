    private void jb_selecVecinoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_selecVecinoMouseClicked
        int pos = vSelec;
        double monto = 0;
        Date fecha = null, mes = new Date();
        //SimpleDateFormat df = new SimpleDateFormat("MM");
  
        monto = Double.parseDouble(tf_monto.getText());
        fecha = dc_fecha.getDate();
        mes.setMonth(mc_mes.getMonth());
        mes.setYear(yc_agregarPago.getYear() - 1900);
        
        vecinos.get(pos).setPago(new Pago(monto, fecha, mes, vecinos.get(pos).getId()));
        String id = "V" + Integer.toString(pos) + "P" + Integer.toString(vecinos.get(pos).getPagos().size() - 1);
        vecinos.get(pos).getPagos().get(vecinos.get(pos).getPagos().size() - 1).setId(id);
        Pago t = vecinos.get(pos).getPagos().get(vecinos.get(pos).getPagos().size() - 1); 
        
        db.conectar();
        try{
            db.query.execute("INSERT INTO pagos" + " (Id,IdVecino,monto,fecha,mes)" + " VALUES ('" + t.getId() + "'," + t.getIdVecino() + "," + t.getMonto() + ",'" + t.getFechaString() + "','" + t.getMesString() + "')");
            db.commit();
        }catch(SQLException ex){
            ex.printStackTrace();
        }
        db.desconectar();
        
        tf_monto.setText("");
        dc_fecha.setDate(null);
        mc_mes.setMonth(new Date().getMonth());
        yc_agregarPago.setYear(new Date().getYear() + 1900);
        
        actualizarTablaPrincipal();
        actualizarInfoEditarVecino();
        JOptionPane.showMessageDialog(this, "Pago agregado exitosamente");
        jd_agregarPago.dispose();
    }//GEN-LAST:event_jb_selecVecinoMouseClicked


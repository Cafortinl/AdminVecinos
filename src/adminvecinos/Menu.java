    public void cargarVecinos(){
        db.conectar();
        try{
            db.query.execute("select Id,tel,nombre,numCasa,cuota,correo,codZona,isEmpresa from vecinos");
            ResultSet rs = db.query.getResultSet();
            try {
                while(rs.next()){
                    vecinos.add(new Vecino(rs.getString(3), rs.getString(4), rs.getString(2), rs.getDouble(5), rs.getString(6), rs.getString(7), rs.getInt(8)));
                    vecinos.get(vecinos.size()-1).setId(rs.getInt(1));
                }
                for (Vecino v : vecinos) {
                    int idVecino = v.getId();
                    db.query.execute("select Id,IdVecino,monto,fecha,mes from pagos where idVecino=" + idVecino);
                    rs = db.query.getResultSet();
                    try{
                        while(rs.next()){
                            v.setPago(new Pago(rs.getDouble(3), rs.getString(4), rs.getString(5), rs.getString(1), rs.getInt(2)));
                            v.getPagos().get(v.getPagos().size()-1).setId(rs.getString(1));
                        }
                    }catch(ParseException exe){
                        exe.printStackTrace();
                    }
                }
                for (Vecino v : vecinos) {
                    int idVecino = v.getId();
                    db.query.execute("select Id,IdVecino,nombre from nombresAlternos where idVecino=" + idVecino);
                    rs = db.query.getResultSet();
                    while(rs.next()){
                        v.setNombreAlt(rs.getString(3));
                    }
                }
            } catch (NullPointerException ex) {
                ex.printStackTrace();
            }
        }catch(SQLException e){
            e.printStackTrace();
        }
        db.desconectar();
    }
    

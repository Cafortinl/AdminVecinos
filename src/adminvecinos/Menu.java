    private void jb_eliminarPagoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_eliminarPagoMouseClicked
        if(pagoSeleccionado() > -1){
            int resp = JOptionPane.showConfirmDialog(this, "Esta seguro que desea eliminar el pago?");
                if(resp == 0){
                    Pago temp = vecinos.get(vecinoSeleccionado()).getPagos().get(pagoSeleccionado());
                    String id = temp.getId();
                    vecinos.get(vecinoSeleccionado()).getPagos().remove(temp);
                    db.conectar();
                    try{
                        db.query.execute("delete from pagos where Id='" + id + "' and IdVecino=" + vecinoSeleccionado());
                        db.commit();
                    
                        for(Pago p : vecinos.get(vecinoSeleccionado()).getPagos()){
                            String nid = "V" + Integer.toString(vecinoSeleccionado()) + "P" + Integer.toString(vecinos.get(vecinoSeleccionado()).getPagos().indexOf(p));
                            db.query.execute("update pagos set Id='" + nid + "' where Id='" + p.getId() + "'");
                            db.commit();
                            p.setId(nid);
                        }
                    }catch(SQLException ex){
                        ex.printStackTrace();
                    }
                    db.desconectar();
                    actualizarInfoEditarVecino();
                }
        }else{
            JOptionPane.showMessageDialog(this, "Debe seleccionar un pago primero");
        }
    }//GEN-LAST:event_jb_eliminarPagoMouseClicked

